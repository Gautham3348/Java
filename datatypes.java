class datatypes {
    public static void main(String[] args) {
        int hello = 10;
        System.out.println(hello);
        float hai = 20;
        System.out.println(hai);
        boolean hey = true;
        System.out.println(hey);
        char hlo = 'H';
        System.out.println(hlo);
        String hi = "Hello Java";
        System.out.println(hi);
    }
}
