class switchs
{
    public static void main(String[] args)
    {
        int num =2;
        switch(num)
        {
            case 6:
                System.out.println("no num");
                break;
            case 1:
                System.out.println("No num");
                break;
            case 2:
                System.out.println("This is number 2 ");
                break;
            default:
                System.out.println("Invalid");
        }
    }
}
